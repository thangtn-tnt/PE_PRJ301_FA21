package BEAN;

/**
 *
 * @author Trương Nhựt Thăng
 */
public class Staff {

    private String id, name, phone, date;

    //Constructor is used to show list from DAO and Database
    public Staff(String id, String name, String phone, String date) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
